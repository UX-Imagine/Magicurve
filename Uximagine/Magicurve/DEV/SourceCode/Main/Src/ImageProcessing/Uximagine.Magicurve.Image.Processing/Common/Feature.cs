namespace Uximagine.Magicurve.Image.Processing.Common
{
    /// <summary>
    /// The feature.
    /// </summary>
    public struct Feature
    {
        /// <summary>
        /// Gets or sets the horizontal count.
        /// </summary>
        /// <value>
        /// The horizontal count.
        /// </value>
        public int HorizontalCount { get; set; }

        /// <summary>
        /// Gets or sets the vertical line count.
        /// </summary>
        /// <value>
        /// The vertical line count.
        /// </value>
        public int VerticalLineCount { get; set; }

        /// <summary>
        /// Gets or sets the corners count.
        /// </summary>
        /// <value>
        /// The corners count.
        /// </value>
        public int CornersCount { get; set; }
    }
}
